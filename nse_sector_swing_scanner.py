class ScanXScraper:
    """Scrapes top delivery data from scanx.trade"""

    def __init__(self, url=SCANX_URL):
        self.url = url
        self.used_fallback = False
        self.headers = {
            'User-Agent': 'Mozilla/5.0 (Windows NT 10.0; Win64; x64) AppleWebKit/537.36 (KHTML, like Gecko) Chrome/126.0.0.0 Safari/537.36',
            'Accept': 'text/html,application/xhtml+xml,application/xml;q=0.9,image/webp,*/*;q=0.8',
            'Accept-Language': 'en-US,en;q=0.5',
        }

    def fetch(self):
        """Fetch and parse the scanx top deliveries page."""
        print(f"[SCANX] Fetching data from {self.url}...")
        try:
            response = requests.get(self.url, headers=self.headers, timeout=30)
            response.raise_for_status()
            soup = BeautifulSoup(response.text, 'lxml')
            return self._parse_table(soup)
        except Exception as e:
            print(f"[ERROR] Failed to fetch scanx data: {e}")
            self.used_fallback = True
            return self._fallback_data()

    def _parse_table(self, soup):
        """Parse the HTML table from scanx page."""
        stocks = []

        # Find all table rows or card elements
        # The page uses a card-based layout with stock info
        rows = soup.find_all('tr')

        for row in rows:
            cells = row.find_all(['td', 'th'])
            if len(cells) >= 7:
                try:
                    # Extract text from cells
                    texts = [cell.get_text(strip=True) for cell in cells]

                    # Try to identify stock name (first meaningful cell)
                    name = texts[0].strip()
                    if not name or len(name) < 2:
                        continue

                    # Parse numeric values
                    def parse_num(s):
                        s = s.replace(',', '').replace('%', '').strip()
                        try:
                            return float(s)
                        except:
                            return 0.0

                    # Map columns: Name, LTP, Change, Change%, Volume, Delivery, Deliv%
                    stock = {
                        'name': name,
                        'ltp': parse_num(texts[1]) if len(texts) > 1 else 0,
                        'change': parse_num(texts[2]) if len(texts) > 2 else 0,
                        'change_pct': parse_num(texts[3]) if len(texts) > 3 else 0,
                        'volume': parse_num(texts[4]) if len(texts) > 4 else 0,
                        'delivery_qty': parse_num(texts[5]) if len(texts) > 5 else 0,
                        'delivery_pct': parse_num(texts[6]) if len(texts) > 6 else 0,
                    }

                    # Clean name - extract ticker
                    stock['ticker'] = self._extract_ticker(name)
                    stock['sector'] = STOCK_SECTOR_MAP.get(stock['ticker'], "UNKNOWN")

                    if stock['ltp'] > 0:
                        stocks.append(stock)

                except Exception as e:
                    continue

        if not stocks:
            # Try alternative parsing - look for div/card structures
            cards = soup.find_all('div', class_=lambda x: x and 'stock' in str(x).lower())
            for card in cards:
                # Fallback parsing logic
                pass

        if not stocks:
            self.used_fallback = True
            return self._fallback_data()

        print(f"[SCANX] Parsed {len(stocks)} stocks from scanx.trade")
        return pd.DataFrame(stocks)

