class SectorAnalyzer:
    """Analyzes sector performance over 2 weeks."""

    def __init__(self):
        self.ta = TechnicalAnalyzer()

    def analyze_sector(self, sector_name, tickers):
        """Analyze a sector's 2-week performance."""
        returns = []
        volumes = []

        for ticker in tickers:
            try:
                nse_ticker = ticker + '.NS'
                stock = yf.Ticker(nse_ticker)
                df = stock.history(period='1mo', interval='1d')
                if len(df) >= 10:
                    two_week_return = ((df['Close'].iloc[-1] - df['Close'].iloc[-10]) / df['Close'].iloc[-10]) * 100
                    returns.append(two_week_return)

                    # Volume trend
                    recent_vol = df['Volume'].iloc[-5:].mean()
                    prev_vol = df['Volume'].iloc[-15:-5].mean()
                    vol_change = ((recent_vol - prev_vol) / prev_vol) * 100 if prev_vol > 0 else 0
                    volumes.append(vol_change)
            except:
                continue

        if not returns:
            return None

        return {
            'sector': sector_name,
            'sector_full': SECTOR_NAMES.get(sector_name, sector_name),
            'avg_return_2w': round(np.mean(returns), 2),
            'median_return_2w': round(np.median(returns), 2),
            'best_stock_return': round(max(returns), 2),
            'worst_stock_return': round(min(returns), 2),
            'stocks_count': len(returns),
            'avg_volume_change': round(np.mean(volumes), 2) if volumes else 0,
            'momentum_score': round(np.mean(returns) + (np.mean(volumes) * 0.1), 2),
        }


