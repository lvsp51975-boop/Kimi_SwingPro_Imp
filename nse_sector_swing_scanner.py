class SwingTradeAnalyzer:
    """Analyzes stocks for swing trade opportunities."""

    def __init__(self, period='3mo', interval='1d'):
        self.period = period
        self.interval = interval
        self.ta = TechnicalAnalyzer()

    def fetch_data(self, ticker):
        """Fetch historical data from Yahoo Finance."""
        try:
            # Add .NS suffix for NSE stocks
            nse_ticker = ticker + '.NS' if not ticker.endswith('.NS') else ticker
            stock = yf.Ticker(nse_ticker)
            df = stock.history(period=self.period, interval=self.interval)
            if df.empty or len(df) < 30:
                return None
            return df
        except Exception as e:
            return None

    def analyze(self, ticker, scanx_data=None):
        """Full technical analysis for a stock."""
        df = self.fetch_data(ticker)
        if df is None:
            return None

        result = {
            'ticker': ticker,
            'name': ticker.replace('.NS', ''),
            'last_price': round(df['Close'].iloc[-1], 2),
            'date': df.index[-1].strftime('%Y-%m-%d'),
        }

        # Add scanx data if available
        if scanx_data is not None:
            result['delivery_pct'] = scanx_data.get('delivery_pct', 0)
            result['delivery_qty'] = scanx_data.get('delivery_qty', 0)
            result['volume_scanx'] = scanx_data.get('volume', 0)
            result['change_pct_today'] = scanx_data.get('change_pct', 0)

        # === CALCULATE ALL INDICATORS ===
        df['EMA_9'] = self.ta.ema(df['Close'], 9)
        df['EMA_21'] = self.ta.ema(df['Close'], 21)
        df['EMA_50'] = self.ta.ema(df['Close'], 50)
        df['SMA_200'] = self.ta.sma(df['Close'], 200)

        df['RSI'] = self.ta.rsi(df['Close'], 14)
        df['MACD'], df['MACD_Signal'], df['MACD_Hist'] = self.ta.macd(df['Close'])
        df['ATR'] = self.ta.atr(df['High'], df['Low'], df['Close'], 14)
        df['BB_Upper'], df['BB_Mid'], df['BB_Lower'] = self.ta.bollinger_bands(df['Close'])
        df['ADX'], df['PLUS_DI'], df['MINUS_DI'] = self.ta.adx(df['High'], df['Low'], df['Close'])
        df['OBV'] = self.ta.obv(df['Close'], df['Volume'])
        df['OBV_EMA'] = self.ta.ema(df['OBV'], 20)
        df['Stoch_K'], df['Stoch_D'] = self.ta.stochastic(df['High'], df['Low'], df['Close'])
        df['Vol_SMA20'] = self.ta.sma(df['Volume'], 20)

        latest = df.iloc[-1]
        prev = df.iloc[-2] if len(df) > 1 else latest

        # === 2-WEEK PERFORMANCE ===
        two_week_ago = df['Close'].iloc[-10] if len(df) >= 10 else df['Close'].iloc[0]
        result['return_2w'] = round(((latest['Close'] - two_week_ago) / two_week_ago) * 100, 2)

        one_month_ago = df['Close'].iloc[-20] if len(df) >= 20 else df['Close'].iloc[0]
        result['return_1m'] = round(((latest['Close'] - one_month_ago) / one_month_ago) * 100, 2)

        # === SWING TRADE SIGNALS ===
        signals = []
        score = 0

        # 1. EMA Alignment (Trend)
        ema_bull = latest['EMA_9'] > latest['EMA_21'] > latest['EMA_50']
        ema_bear = latest['EMA_9'] < latest['EMA_21'] < latest['EMA_50']
        price_above_200 = latest['Close'] > latest['SMA_200']

        result['ema_bull'] = ema_bull
        result['ema_bear'] = ema_bear
        result['above_200sma'] = price_above_200

        if ema_bull and price_above_200:
            score += 3
            signals.append("Strong Uptrend (9>21>50 + above 200 SMA)")
        elif ema_bull:
            score += 2
            signals.append("Bullish EMA Alignment")
        elif ema_bear:
            score -= 2
            signals.append("Bearish EMA Alignment")

        # 2. EMA Crossover (Entry Signal)
        ema_cross_bull = (prev['EMA_9'] <= prev['EMA_21']) and (latest['EMA_9'] > latest['EMA_21'])
        ema_cross_bear = (prev['EMA_9'] >= prev['EMA_21']) and (latest['EMA_9'] < latest['EMA_21'])

        result['ema_cross_bull'] = ema_cross_bull
        result['ema_cross_bear'] = ema_cross_bear

        if ema_cross_bull:
            score += 4
            signals.append("BULLISH EMA CROSSOVER (9/21)")
        elif ema_cross_bear:
            score -= 4
            signals.append("BEARISH EMA CROSSOVER (9/21)")

        # 3. RSI Momentum
        rsi = latest['RSI']
        result['rsi'] = round(rsi, 2)

        if 50 < rsi < 70:
            score += 2
            signals.append(f"RSI Momentum ({round(rsi,1)})")
        elif 40 < rsi <= 50:
            score += 1
            signals.append(f"RSI Building ({round(rsi,1)})")
        elif rsi < 30 and ema_bull:
            score += 2
            signals.append(f"RSI Oversold Bounce ({round(rsi,1)})")
        elif rsi > 75:
            score -= 1
            signals.append(f"RSI Overbought ({round(rsi,1)})")

        # 4. MACD
        macd_bull_cross = (prev['MACD'] <= prev['MACD_Signal']) and (latest['MACD'] > latest['MACD_Signal'])
        macd_bear_cross = (prev['MACD'] >= prev['MACD_Signal']) and (latest['MACD'] < latest['MACD_Signal'])
        macd_above = latest['MACD'] > latest['MACD_Signal']

        result['macd_bull_cross'] = macd_bull_cross
        result['macd_bear_cross'] = macd_bear_cross
        result['macd_above'] = macd_above

        if macd_bull_cross:
            score += 3
            signals.append("MACD Bullish Crossover")
        elif macd_bear_cross:
            score -= 3
            signals.append("MACD Bearish Crossover")
        elif macd_above:
            score += 1
            signals.append("MACD Above Signal")

        # 5. Volume Confirmation
        vol_spike = latest['Volume'] > latest['Vol_SMA20'] * 1.5
        result['volume_spike'] = vol_spike
        result['volume_ratio'] = round(latest['Volume'] / latest['Vol_SMA20'], 2) if latest['Vol_SMA20'] > 0 else 0

        if vol_spike and (ema_cross_bull or macd_bull_cross):
            score += 2
            signals.append("Volume Confirmation Spike")
        elif vol_spike:
            score += 1
            signals.append("Volume Spike")

        # 6. Bollinger Bands
        near_lower = latest['Close'] <= latest['BB_Lower'] * 1.02
        near_upper = latest['Close'] >= latest['BB_Upper'] * 0.98

        result['near_bb_lower'] = near_lower
        result['near_bb_upper'] = near_upper

        if near_lower and macd_above:
            score += 2
            signals.append("Near BB Lower + MACD Bullish")
        elif near_upper and not macd_above:
            score -= 1
            signals.append("Near BB Upper (Resistance)")

        # 7. ADX (Trend Strength)
        adx_val = latest['ADX']
        result['adx'] = round(adx_val, 2)

        if adx_val > 25:
            if latest['PLUS_DI'] > latest['MINUS_DI']:
                score += 1
                signals.append(f"Strong Bullish Trend (ADX {round(adx_val,1)})")
            else:
                score -= 1
                signals.append(f"Strong Bearish Trend (ADX {round(adx_val,1)})")

        # 8. OBV Confirmation
        obv_bull = latest['OBV'] > latest['OBV_EMA']
        result['obv_bull'] = obv_bull

        if obv_bull and ema_bull:
            score += 1
            signals.append("OBV Confirming Uptrend")

        # 9. Stochastic
        stoch_k = latest['Stoch_K']
        stoch_d = latest['Stoch_D']
        result['stoch_k'] = round(stoch_k, 2)
        result['stoch_d'] = round(stoch_d, 2)

        if stoch_k > stoch_d and stoch_k < 80:
            score += 1
            signals.append("Stochastic Bullish")

        # === DELIVERY DATA BOOST ===
        if 'delivery_pct' in result and result['delivery_pct'] > 70:
            score += 1
            signals.append(f"High Delivery {result['delivery_pct']}%")

        # === ATR & RISK MANAGEMENT ===
        atr = latest['ATR']
        result['atr'] = round(atr, 2)
        result['atr_pct'] = round((atr / latest['Close']) * 100, 2)

        if score >= 5:
            result['stop_loss'] = round(latest['Close'] - (2 * atr), 2)
            result['target_1'] = round(latest['Close'] + (2 * atr), 2)
            result['target_2'] = round(latest['Close'] + (3.5 * atr), 2)
            result['risk_reward'] = round((result['target_1'] - latest['Close']) / (latest['Close'] - result['stop_loss']), 2)
        elif score <= -5:
            result['stop_loss'] = round(latest['Close'] + (2 * atr), 2)
            result['target_1'] = round(latest['Close'] - (2 * atr), 2)
            result['target_2'] = round(latest['Close'] - (3.5 * atr), 2)
            result['risk_reward'] = round((latest['Close'] - result['target_1']) / (result['stop_loss'] - latest['Close']), 2)
        else:
            result['stop_loss'] = None
            result['target_1'] = None
            result['target_2'] = None
            result['risk_reward'] = None

        # === FINAL CLASSIFICATION ===
        result['score'] = score
        result['signals'] = signals

        if score >= 7:
            result['rating'] = "STRONG BUY"
        elif score >= 5:
            result['rating'] = "BUY"
        elif score >= 3:
            result['rating'] = "WEAK BUY"
        elif score <= -7:
            result['rating'] = "STRONG SELL"
        elif score <= -5:
            result['rating'] = "SELL"
        elif score <= -3:
            result['rating'] = "WEAK SELL"
        else:
            result['rating'] = "NEUTRAL"

        return result


